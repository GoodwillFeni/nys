#!/usr/bin/env node
/*
 * Walks src/store/router.js, extracts every route name + path, and emits the
 * permissions registry to three locations: web (JSON), API (PHP), app (TS).
 *
 * Called via `npm run build:perms` or as a pre-build step. Idempotent.
 *
 * Output shape:
 *   {
 *     routes: [{ name, path, group, label }],
 *     actions: [{ name, label }]
 *   }
 *
 * Groups are inferred from path prefix:
 *   /Farm/*     -> Farm
 *   /Shop/*     -> Shop
 *   /Customer/* -> Customer
 *   /Device*    -> Devices
 *   /UserList, /AddUser, /EditUser, /AuditLogs -> Users & Admin
 *   /AccountList, /AddAccount, /EditAccount  -> Users & Admin
 *   /           -> Dashboard
 *   auth pages (LogIn/SignUp/Forgot/Reset) are skipped (public)
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const REPO_ROOT = path.resolve(ROOT, '..');

const ROUTER_FILE = path.join(ROOT, 'src', 'store', 'router.js');
const WEB_OUT = path.join(ROOT, 'src', 'permissions-registry.json');
const API_OUT = path.join(REPO_ROOT, 'nkunziyenugu_api', 'config', 'permissions_registry.php');
const APP_OUT = path.join(REPO_ROOT, 'nys_app', 'src', 'permissions-registry.ts');
// New (Chunk 4): typed const maps so callers get compile-time-checked
// permission names. Avoids the silent-rename bug where 'ShopDashboard'
// → 'ShopHome' in one file but stays as the old string everywhere else.
const WEB_CONST_OUT = path.join(ROOT, 'src', 'constants', 'permissions.js');
const APP_CONST_OUT = path.join(REPO_ROOT, 'nys_app', 'src', 'constants', 'permissions.ts');

const ACTIONS = [
  { name: 'view',     label: 'View' },
  { name: 'add',      label: 'Add' },
  { name: 'edit',     label: 'Edit' },
  { name: 'delete',   label: 'Delete' },
  { name: 'approve',  label: 'Approve' },
  { name: 'complete', label: 'Complete' },
  { name: 'assign',   label: 'Assign / link' },
  { name: 'configure',label: 'Configure' },
];

function groupFor(routePath) {
  if (routePath === '/' || routePath === '') return 'Dashboard';
  if (routePath.startsWith('/Farm')) return 'Farm';
  if (routePath.startsWith('/Shop')) return 'Shop';
  if (routePath.startsWith('/Customer')) return 'Customer';
  if (routePath.startsWith('/Device')) return 'Devices';
  if (routePath.startsWith('/User') || routePath.startsWith('/AddUser') || routePath.startsWith('/EditUser')) return 'Users & Admin';
  if (routePath.startsWith('/Account') || routePath.startsWith('/AddAccount') || routePath.startsWith('/EditAccount')) return 'Users & Admin';
  if (routePath.startsWith('/Audit')) return 'Users & Admin';
  return 'Other';
}

function humanize(name) {
  // "AddAnimalEvent" -> "Add Animal Event"
  return name
    .replace(/([a-z])([A-Z])/g, '$1 $2')
    .replace(/([A-Z])([A-Z][a-z])/g, '$1 $2');
}

function extractRoutes(src) {
  const routes = [];
  // Match each route block: { path: '...', name: '...', ..., meta: { ... } }
  // Our strategy: find every "name:" occurrence, look backwards for the path, forward for guestOnly.
  const routeRegex = /\{\s*path:\s*'([^']+)'[\s\S]*?name:\s*'([^']+)'[\s\S]*?meta:\s*\{([^}]*)\}/g;
  let m;
  while ((m = routeRegex.exec(src)) !== null) {
    const [, routePath, routeName, metaBlock] = m;
    const guestOnly = /guestOnly\s*:\s*true/.test(metaBlock);
    if (guestOnly) continue; // skip public auth pages
    routes.push({
      name: routeName,
      path: routePath,
      group: groupFor(routePath),
      label: humanize(routeName),
    });
  }
  return routes;
}

function writeJson(file, data) {
  fs.mkdirSync(path.dirname(file), { recursive: true });
  fs.writeFileSync(file, JSON.stringify(data, null, 2) + '\n');
  console.log('  wrote', path.relative(REPO_ROOT, file));
}

function writePhp(file, data) {
  const esc = (s) => String(s).replace(/'/g, "\\'");
  const routesPhp = data.routes.map(r =>
    `    ['name' => '${esc(r.name)}', 'path' => '${esc(r.path)}', 'group' => '${esc(r.group)}', 'label' => '${esc(r.label)}'],`
  ).join('\n');
  const actionsPhp = data.actions.map(a =>
    `    ['name' => '${esc(a.name)}', 'label' => '${esc(a.label)}'],`
  ).join('\n');
  const out = `<?php
/**
 * Auto-generated by nkunziyenugu_systems/scripts/build-permissions-registry.js
 * Do NOT edit by hand — regenerate with: npm run build:perms
 */

return [
  'routes' => [
${routesPhp}
  ],
  'actions' => [
${actionsPhp}
  ],
];
`;
  fs.mkdirSync(path.dirname(file), { recursive: true });
  fs.writeFileSync(file, out);
  console.log('  wrote', path.relative(REPO_ROOT, file));
}

function writeTs(file, data) {
  const out = `// Auto-generated by nkunziyenugu_systems/scripts/build-permissions-registry.js
// Do NOT edit by hand — regenerate with: npm run build:perms

export interface RouteEntry { name: string; path: string; group: string; label: string; }
export interface ActionEntry { name: string; label: string; }

export const ROUTES: RouteEntry[] = ${JSON.stringify(data.routes, null, 2)};

export const ACTIONS: ActionEntry[] = ${JSON.stringify(data.actions, null, 2)};

export const ROUTE_NAMES = ROUTES.map(r => r.name);
export const ACTION_NAMES = ACTIONS.map(a => a.name);

export type RouteName = typeof ROUTE_NAMES[number];
export type ActionName = typeof ACTION_NAMES[number];
`;
  fs.mkdirSync(path.dirname(file), { recursive: true });
  fs.writeFileSync(file, out);
  console.log('  wrote', path.relative(REPO_ROOT, file));
}

/**
 * Typed const maps — `P` for routes, `A` for actions. Lets callers write
 *   canRoute(P.ShopDashboard)
 * instead of
 *   canRoute('ShopDashboard')
 * so a typo or rename breaks at compile time, not silently at runtime.
 */
function writeConstMaps(file, data, lang /* 'js' | 'ts' */) {
  const routesObj = data.routes.map(r => `  ${r.name}: '${r.name}'`).join(',\n');
  const actionsObj = data.actions.map(a => `  ${a.name}: '${a.name}'`).join(',\n');
  const header = `// Auto-generated by nkunziyenugu_systems/scripts/build-permissions-registry.js
// Do NOT edit by hand — regenerate with: npm run build:perms
//
// Typed name maps so callers get compile-time-checked permission names.
// Use:  canRoute(P.ShopDashboard)  /  canAction(A.view)
`;
  let body;
  if (lang === 'ts') {
    body = `
export const P = {
${routesObj}
} as const;

export const A = {
${actionsObj}
} as const;

export type RouteName = typeof P[keyof typeof P];
export type ActionName = typeof A[keyof typeof A];
`;
  } else {
    // JS for the Vue project (no TS config there). Object.freeze gives
    // runtime immutability; const naming covers IDE autocomplete.
    body = `
export const P = Object.freeze({
${routesObj}
});

export const A = Object.freeze({
${actionsObj}
});
`;
  }
  fs.mkdirSync(path.dirname(file), { recursive: true });
  fs.writeFileSync(file, header + body);
  console.log('  wrote', path.relative(REPO_ROOT, file));
}

function main() {
  const src = fs.readFileSync(ROUTER_FILE, 'utf8');
  const routes = extractRoutes(src);
  const data = { routes, actions: ACTIONS };

  console.log(`Extracted ${routes.length} protected routes from router.js`);
  writeJson(WEB_OUT, data);
  writePhp(API_OUT, data);
  writeTs(APP_OUT, data);
  writeConstMaps(WEB_CONST_OUT, data, 'js');
  writeConstMaps(APP_CONST_OUT, data, 'ts');
  console.log('Done.');
}

main();
